#include "libft.h"

static int	in_set(char c, const char *set)
{
	while (*set)
	{
		if (*set == c)
			return (1);
		set++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*s2;
	size_t	start;
	size_t	end;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && in_set(s1[start], set))
		start++;
	end = ft_strlen(s1);
	while (end > start && in_set(s1[end - 1], set))
		end--;
	s2 = malloc(sizeof(char) * (end - start) + 1);
	if (!s2)
		return (NULL);
	ft_memcpy(s2, s1 + start, end - start);
	s2[end - start] = '\0';
	return (s2);
}
